export const useTranslation = () => {
    const { user } = useAuthStore();
    const lang = user?.language || 'en';

    const t = (key: keyof typeof translations['en']) => {
        return (translations as any)[lang]?.[key] || (translations as any)['en'][key] || key;
    };

    const isRTL = lang === 'ar';
    return { t, isRTL, lang };
};
